import * as crypto from 'crypto';

/**
 * Generate a random email address for testing
 */
export function generateTestEmail(prefix: string = 'testuser'): string {
  const timestamp = Date.now();
  const random = crypto.randomBytes(4).toString('hex');
  return `${prefix}-${timestamp}-${random}@e2e-test.local`;
}

/**
 * Generate a secure random password
 */
export function generateTestPassword(): string {
  const chars = 'abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789!@#$%';
  let password = '';
  for (let i = 0; i < 16; i++) {
    password += chars.charAt(Math.floor(Math.random() * chars.length));
  }
  // Ensure at least one of each required character type
  return password + 'Aa1!';
}

/**
 * Generate mock crypto keys for testing
 *
 * In a real scenario, these would be generated by the client-side crypto library.
 * For E2E testing, we use placeholder values that the server accepts.
 */
export function generateMockCryptoKeys() {
  // Generate random bytes and encode as base64
  const kemPublicKey = crypto.randomBytes(1184).toString('base64'); // ML-KEM-768 public key size
  const signPublicKey = crypto.randomBytes(1312).toString('base64'); // ML-DSA-65 public key size
  const encryptedMasterKey = crypto.randomBytes(48).toString('base64'); // AES-256-GCM encrypted key
  const masterKeyNonce = crypto.randomBytes(12).toString('base64'); // GCM nonce

  return {
    public_keys: {
      kem: kemPublicKey,
      sign: signPublicKey,
    },
    encrypted_master_key: encryptedMasterKey,
    master_key_nonce: masterKeyNonce,
  };
}

