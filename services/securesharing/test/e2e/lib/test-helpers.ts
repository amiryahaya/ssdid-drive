/**
 * Wait utility with exponential backoff
 */
export async function waitWithBackoff(
  checkFn: () => Promise<boolean>,
  options: {
    maxAttempts?: number;
    initialDelayMs?: number;
    maxDelayMs?: number;
    backoffMultiplier?: number;
  } = {}
): Promise<void> {
  const {
    maxAttempts = 10,
    initialDelayMs = 1000,
    maxDelayMs = 30000,
    backoffMultiplier = 2,
  } = options;

  let delay = initialDelayMs;

  for (let attempt = 1; attempt <= maxAttempts; attempt++) {
    const success = await checkFn();
    if (success) {
      return;
    }

    if (attempt < maxAttempts) {
      await new Promise((resolve) => setTimeout(resolve, delay));
      delay = Math.min(delay * backoffMultiplier, maxDelayMs);
    }
  }

  throw new Error(`Condition not met after ${maxAttempts} attempts`);
}

/**
 * Retry a function with exponential backoff
 */
export async function retryWithBackoff<T>(
  fn: () => Promise<T>,
  options: {
    maxAttempts?: number;
    initialDelayMs?: number;
    maxDelayMs?: number;
    backoffMultiplier?: number;
    shouldRetry?: (error: Error) => boolean;
  } = {}
): Promise<T> {
  const {
    maxAttempts = 3,
    initialDelayMs = 1000,
    maxDelayMs = 10000,
    backoffMultiplier = 2,
    shouldRetry = () => true,
  } = options;

  let delay = initialDelayMs;
  let lastError: Error | undefined;

  for (let attempt = 1; attempt <= maxAttempts; attempt++) {
    try {
      return await fn();
    } catch (error) {
      lastError = error as Error;

      if (attempt >= maxAttempts || !shouldRetry(lastError)) {
        throw lastError;
      }

      await new Promise((resolve) => setTimeout(resolve, delay));
      delay = Math.min(delay * backoffMultiplier, maxDelayMs);
    }
  }

  throw lastError!;
}

/**
 * Assert that text contains expected redaction markers
 */
export function assertRedacted(
  redactedText: string,
  originalText: string,
  expectedTypes: string[]
): void {
  // Check that original PII values are not present
  const piiPatterns = [
    /\b\d{3}-\d{2}-\d{4}\b/, // SSN
    /\b[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Z|a-z]{2,}\b/, // Email
    /\b\d{4}-\d{4}-\d{4}-\d{4}\b/, // Credit card
    /\(\d{3}\)\s*\d{3}-\d{4}/, // Phone
  ];

  for (const pattern of piiPatterns) {
    const originalMatches = originalText.match(pattern);
    if (originalMatches) {
      for (const match of originalMatches) {
        if (redactedText.includes(match)) {
          throw new Error(`PII value "${match}" was not redacted`);
        }
      }
    }
  }

  // Check for redaction markers
  const redactionMarkerPattern = /\[([A-Z_]+)\]/g;
  const foundMarkers = [...redactedText.matchAll(redactionMarkerPattern)].map((m) => m[1]);

  for (const expectedType of expectedTypes) {
    if (!foundMarkers.some((m) => m.includes(expectedType) || expectedType.includes(m))) {
      console.warn(
        `Expected redaction marker for ${expectedType} not found. Found: ${foundMarkers.join(', ')}`
      );
    }
  }
}
