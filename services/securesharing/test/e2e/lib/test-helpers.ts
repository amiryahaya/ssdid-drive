/**
 * Generate a tiered KDF salt: [profile_byte(1)] || [random(16)] = 17 bytes, base64-encoded
 */
export function generateTieredSalt(profileByte: number): string {
  const salt = Buffer.alloc(17);
  salt[0] = profileByte;
  crypto.randomBytes(16).copy(salt, 1);
  return salt.toString('base64');
}

/**
 * Generate mock crypto keys with a specific KDF profile salt
 */
export function generateMockCryptoKeysWithSalt(profileByte: number) {
  return {
    ...generateMockCryptoKeys(),
    key_derivation_salt: generateTieredSalt(profileByte),
  };
}

/**
 * Generate mock file metadata for upload testing
 */
export function generateMockFileMetadata(filename: string, content: string | Buffer) {
  const contentBuffer = Buffer.isBuffer(content) ? content : Buffer.from(content);
  const blobHash = crypto.createHash('sha256').update(contentBuffer).digest('hex');

  return {
    filename,
    content_type: 'text/plain',
    size: contentBuffer.length,
    encrypted_metadata: crypto.randomBytes(256).toString('base64'),
    metadata_nonce: crypto.randomBytes(12).toString('base64'),
    wrapped_dek: crypto.randomBytes(48).toString('base64'),
    kem_ciphertext: crypto.randomBytes(1088).toString('base64'), // ML-KEM-768 ciphertext size
    blob_hash: blobHash,
    signature: crypto.randomBytes(3309).toString('base64'), // ML-DSA-65 signature size
  };
}

